    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Disciplina disciplinaEscolhida = this.getObjetoSelecionadoNaGrid();

if (disciplinaEscolhida == null) {
    JOptionPane.showMessageDialog(this, "Selecione uma disciplina para excluir.");
    return;
}

String nomeEscolhido = disciplinaEscolhida.getCod();

if (nomeEscolhido == null || nomeEscolhido.trim().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Código da disciplina inválido.");
    return;
}

Disciplina a = disciplinaController.buscarDisciplina(nomeEscolhido);

if (a == null) {
    JOptionPane.showMessageDialog(this, "Não existe disciplina com este código.");
} else {
    disciplinaController.removerDisciplina(nomeEscolhido);
    JOptionPane.showMessageDialog(this, "Exclusão feita com sucesso!");
}

this.atualizarTabela();
    }//GEN-LAST:event_btnExcluirActionPerformed

