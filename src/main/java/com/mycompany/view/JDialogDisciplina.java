    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
    String cod = edtCod.getText().trim();
    String nome = edtNome.getText().trim();
    String semestre = edtSemestre.getText().trim();
    String horario = edtHorario.getText().trim();

    if (cod.isEmpty()) {
        JOptionPane.showMessageDialog(this, "O campo Código deve ser preenchido.", "Erro", JOptionPane.ERROR_MESSAGE);
        edtCod.requestFocus();
        return;
    }
    if (nome.isEmpty()) {
        JOptionPane.showMessageDialog(this, "O campo Nome deve ser preenchido.", "Erro", JOptionPane.ERROR_MESSAGE);
        edtNome.requestFocus();
        return;
    }
    // ... faça o mesmo para semestre e horario, se quiser validar todos

    try {
        if (this.editando) {
            this.disciplinaController.atualizarDisciplina(codAntigo, nome, semestre, horario);
        } else {
            this.disciplinaController.adicionarDisciplina(cod, nome, semestre, horario);
        }
        this.limparCampos();
        this.habilitarCampos(false);
        this.atualizarTabela();
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Erro ao salvar disciplina: " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnMatricularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMatricularActionPerformed
    Disciplina disciplinaEscolhida = this.getObjetoSelecionadoNaGrid();

    if (disciplinaEscolhida == null) {
        JOptionPane.showMessageDialog(this, "Disciplina não existe na escola");
    } else {
        JDialogSelectALuno tela = null;
        try {
            // Obtém o frame pai da janela atual e passa para o diálogo
            java.awt.Frame framePai = (java.awt.Frame) javax.swing.SwingUtilities.getWindowAncestor(this);
            tela = new JDialogSelectALuno(framePai, true, disciplinaEscolhida);
        } catch (SQLException ex) {
            Logger.getLogger(JDialogSelectProfessor.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (tela != null) {
            tela.setVisible(true);
        }
        atualizarTabela();
    }
    }//GEN-LAST:event_btnMatricularActionPerformed

